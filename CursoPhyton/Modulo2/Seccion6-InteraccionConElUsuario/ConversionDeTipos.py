Numero1=float(input("Escribe el numero que sera la base: ")) #la funcion input de manera flotante permite que el usuario interactue con numeros
Numero2=float(input("Escribe el numero que sera la potencia: "))

R= Numero1 ** Numero2 #Variable llamada R que guarda la operacion indicada
print("Tu base seleccionada es: ", Numero1) #se muestra el numero escrito por el usuario
print("Tu base se va a elevar a la potencia: ", Numero2) #se muestra el segundo numero escrito por el usuario
print("El resultado de esta operacion es: ", R) #se nuestra el resultado llamando a la variable R