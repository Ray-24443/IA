contador=0 #variable con valor de 0
while contador !=6:#mientras que el contador no sea 6
   contador +=1 #mientras se cumpla el while el contador ira incrementando a 1 a su valor anterior
   print("Ejecucion numero: ", contador)#se muestra el numero guardado del contador hasta llegar a 6
    
print("Fin del bucle, la ejecucion ha llegado a: ",contador)#cuando se llega a 6 se sale del bucle e imprime el final