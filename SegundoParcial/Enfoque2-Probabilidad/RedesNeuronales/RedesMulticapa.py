# RED MULTICAPA
# ==========================================

# Entradas
x1 = 1
x2 = 0

# Primera capa
h1 = (x1 * 0.5) + (x2 * 0.4)
h2 = (x1 * 0.3) + (x2 * 0.7)

# Segunda capa
salida = (h1 * 0.8) + (h2 * 0.6)

# Resultado
print("Salida multicapa")
print(salida)   